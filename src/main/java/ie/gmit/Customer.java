package ie.gmit;

public class Customer {

    private String Name;
    private  String PPS;
    private String Address;
    private String Email;
    private String Phone;

    public Customer(String name, String pps, String address, String email, String phone){
        this.Name = name;
        this.PPS = pps;
        this.Address = address;
        this.Email = email;
        this.Phone = phone;
    }


}
